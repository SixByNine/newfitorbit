    private void updateParams() {
        try {
            fitter.getParams().p0 = Double.parseDouble(this.jTextField_period.getText());
            fitter.getParams().p1 = Double.parseDouble(this.jTextField_pdot.getText());
            fitter.getParams().pb = Double.parseDouble(this.jTextField_pb.getText());
            fitter.getParams().x = Double.parseDouble(this.jTextField_x.getText());
            fitter.getParams().epoch = Double.parseDouble(this.jTextField_epoch.getText());
            fitter.getParams().tasc = Double.parseDouble(this.jTextField_tasc.getText());
            fitter.getParams().omega = Double.parseDouble(this.jTextField_omega.getText());
            fitter.getParams().e = Double.parseDouble(this.jTextField_ecc.getText());

            fitter.setStart(Double.parseDouble(this.jTextField_start.getText()));
            fitter.setEnd(Double.parseDouble(this.jTextField_end.getText()));

            fitter.getParams().fitEpoch = this.jCheckBox_epoch.isSelected();
            fitter.getParams().fitP0 = this.jCheckBox_period.isSelected();
            fitter.getParams().fitP1 = this.jCheckBox_pdot.isSelected();
            fitter.getParams().fitPb = this.jCheckBox_pb.isSelected();
            fitter.getParams().fitX = this.jCheckBox_x.isSelected();
            fitter.getParams().fitTasc = this.jCheckBox_tasc.isSelected();
            fitter.getParams().fitOmega = this.jCheckBox_omega.isSelected();
            fitter.getParams().fitE = this.jCheckBox_ecc.isSelected();


        } catch (Exception e) {
            this.jLabel_status.setText("Bad parameter!");

        }

    }

