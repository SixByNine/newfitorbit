    private void replot() {

        this.jTextField_period.setText(String.valueOf(fitter.getParams().p0));
        this.jTextField_pdot.setText(String.valueOf(fitter.getParams().p1));
        this.jTextField_pb.setText(String.valueOf(fitter.getParams().pb));
        this.jTextField_x.setText(String.valueOf(fitter.getParams().x));
        this.jTextField_epoch.setText(String.valueOf(fitter.getParams().epoch));
        this.jTextField_tasc.setText(String.valueOf(fitter.getParams().tasc));
        this.jTextField_omega.setText(String.valueOf(fitter.getParams().omega));
        this.jTextField_ecc.setText(String.valueOf(fitter.getParams().e));

        this.jTextField_start.setText(String.valueOf(this.fitter.getStart()));
        this.jTextField_end.setText(String.valueOf(this.fitter.getEnd()));

        this.jPanel_plot.removeAll();
        String ylab = "Period (us) - " + fitter.getParams().p0;
        double[][] xys = this.fitter.getFitCurve(128);
        for (int i = 0; i < xys[0].length; i++) {
            xys[0][i] = xys[0][i] - fitter.getParams().epoch;
            xys[1][i] = (xys[1][i] - fitter.getParams().p0)*1e6;
        }

        double[] mjds = fitter.getPerfile().getMjds();
        double[] periods = fitter.getPerfile().getPeriods();
        for (int i = 0; i < mjds.length; i++) {
            mjds[i] = mjds[i] - fitter.getParams().epoch;
            periods[i] = (periods[i] - fitter.getParams().p0)*1e6;
        }


        if (this.jToggleButton2.isSelected()) {
            double[] modelPeriods = new OrbitalFunction(fitter.getParams()).generateData(fitter.getPerfile().getMjds(), fitter.getParams().getParmArray());
            for (int i = 0; i < mjds.length; i++) {
                periods[i] = (periods[i]/1e6 - (modelPeriods[i] - fitter.getParams().p0))*1e6;
            }
            for (int i = 0; i < xys[0].length; i++) {
                xys[1][i] = 0;
            }
            ylab="Residual (us)";
        }


        final MKPlot plot = new PlotOneDim(fitter.getParfile().getPsrj(), "MJD - " + fitter.getParams().epoch, ylab , xys[0], xys[1], mjds, periods, Color.red, Color.blue);
        plot.setLinestyle(MKPlot.LineStyle.JoinTheDots);
        if (!this.jToggleButton1.isSelected()) {
            double[] errs=this.fitter.getPerfile().getPerrors();
            for (int i = 0; i < mjds.length; i++) {
                errs[i]*=1e6;
            }
            plot.setYErrors(errs);
        }
        plot.addMouseListener(new MouseListener() {

            public void mouseClicked(final MouseEvent e) {
                SwingUtilities.invokeLater(new Runnable() {

                    public void run() {
                        double x = plot.getXPlotPosn(e.getPoint().x);
                        double y = plot.getYPlotPosn(e.getPoint().y);
                        if (e.getButton() == MouseEvent.BUTTON1) {
                            PulsarFitterGui.this.leftClicked(x, y);
                        }
                        if (e.getButton() == MouseEvent.BUTTON3) {
                            double xmin = plot.getXPlotPosn(e.getPoint().x - 16);
                            double ymin = plot.getYPlotPosn(e.getPoint().y + 16);
                            double xmax = plot.getXPlotPosn(e.getPoint().x + 16);
                            double ymax = plot.getYPlotPosn(e.getPoint().y - 16);
//                            plot.setVals(new double[]{xmin, xmin, xmax, xmax}, new double[]{ymin, ymax, ymax, ymin});
                            PulsarFitterGui.this.fitter.getPerfile().delete(x, y, xmin, xmax, ymin, ymax);
                            PulsarFitterGui.this.replot();
                        }
                    }
                });



            }

            public void mousePressed(MouseEvent e) {
            }

            public void mouseReleased(MouseEvent e) {
            }

            public void mouseEntered(MouseEvent e) {
            }

            public void mouseExited(MouseEvent e) {
            }
        });



        this.jPanel_plot.add(plot, BorderLayout.CENTER);
        plot.setVisible(true);
        this.validate();
        this.repaint();
    }

    private void leftClicked(double x, double y) {
        if (this.jRadioButton_start.isSelected()) {
            prevStart = fitter.getStart();
            prevEnd = fitter.getEnd();
            this.jTextField_start.setText(String.valueOf(x + fitter.getParams().epoch));
            this.jRadioButton_end.setSelected(true);
            this.updateParams();
        } else if (this.jRadioButton_end.isSelected()) {
            prevStart = fitter.getStart();
            prevEnd = fitter.getEnd();
            this.jTextField_end.setText(String.valueOf(x + fitter.getParams().epoch));
            this.jRadioButton_start.setSelected(true);
            this.updateParams();
        }
        replot();
    }

